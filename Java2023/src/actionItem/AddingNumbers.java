package actionItem;

public class AddingNumbers {

	public static void main(String[] args) {
		//adding all numbers from 5 to 15
		int sum = 0;
		for (int i =0; i<15; i++)
		{
			sum=sum	+1;
		}
		System.out.println("the result is:" +sum);
		
		int sum1 =0;
		int z =0;
		while (z<=15)
		{
			sum1 = sum1 + z;
			z ++;
		}
		
		System.out.println("The result is:" + sum1);
		}

	}


