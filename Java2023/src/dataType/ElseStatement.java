package dataType;

public class ElseStatement {

	public static void main(String[] args) {
	//   code to be executed if the condition is false
			int time = 20;
			if (time < 18) {
			  System.out.println("Good day.");
			} else {
			  System.out.println("Good evening.");
			}
			// Outputs "Good evening."
		}

	}
