package array;

public class two_D_array {

	public static void main(String[] args) {
		int [][] my2dArray = new int [2][3];
		 
		
		my2dArray[0][0]=5;
		my2dArray[0][1]=6;
		my2dArray[0][2]=7;
		
		my2dArray[1][0]=8;
		my2dArray[1][1]=6;
		my2dArray[1][2]=7;
		System.out.println(my2dArray[1][1]);

	}
}